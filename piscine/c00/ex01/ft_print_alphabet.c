#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	min;

	min = 'a';
	while (min <= 'z')
	{
		ft_putchar(min);
		min++;
	}
}
