#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	min;

	min = 'z';
	while (min >= 'a')
	{
		ft_putchar(min);
		min--;
	}
}
