int	ft_sqrt(int nb)
{
	int	i;
	int	nbsqrt;

	i = 0;
	while (i < nb)
	{
		nbsqrt = i * i;
		if (nbsqrt == nb)
			return (i);
		i++;
	}
	return (0);
}
