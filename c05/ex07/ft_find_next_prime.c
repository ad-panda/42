int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb == 0 || nb == 1)
		return (0);
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	j;

	j = nb;
	if (ft_is_prime(j) == 1)
		return (j);
	while (ft_is_prime(j) == 0)
		j++;
	return (j);
}
