#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	negative;
	int	nb;

	nb = 0;
	negative = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == 45 || str[i] == 43)
	{
		if (str[i] == '-')
			negative *= (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb *= 10;
		nb += (str[i] - '0');
		i++;
	}
	return (nb * negative);
}